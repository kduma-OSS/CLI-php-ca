<?php

declare(strict_types=1);

use KDuma\PhpCA\Record\Extension\Extensions\AuthorityInfoAccessExtension;
use KDuma\PhpCA\Record\Extension\Extensions\CrlDistributionPointsExtension;
use KDuma\PhpCA\Record\Extension\Extensions\NetscapeCommentExtension;
use KDuma\PhpCA\Record\Extension\Extensions\PrivateKeyUsagePeriodExtension;

// --- CrlDistributionPointsExtension ---

test('CrlDistributionPoints: constructor sets URIs', function () {
    $ext = new CrlDistributionPointsExtension(uris: ['http://crl.example.com/ca.crl', 'http://crl2.example.com/ca.crl']);

    expect($ext->uris)->toBe(['http://crl.example.com/ca.crl', 'http://crl2.example.com/ca.crl'])
        ->and($ext->isCritical())->toBeFalse();
});

test('CrlDistributionPoints: oid and name', function () {
    expect(CrlDistributionPointsExtension::oid())->toBe('2.5.29.31')
        ->and(CrlDistributionPointsExtension::name())->toBe('crl-distribution-points');
});

test('CrlDistributionPoints: toArray/fromArray round-trip', function () {
    $original = new CrlDistributionPointsExtension(
        uris: ['http://crl.example.com/ca.crl'],
        critical: true,
    );
    $array = $original->toArray();
    $restored = CrlDistributionPointsExtension::fromArray($array);

    expect($restored->uris)->toBe($original->uris)
        ->and($restored->isCritical())->toBe($original->isCritical())
        ->and($array)->toBe([
            'name' => 'crl-distribution-points',
            'critical' => true,
            'uris' => ['http://crl.example.com/ca.crl'],
        ]);
});

test('CrlDistributionPoints: fromArray with empty data', function () {
    $ext = CrlDistributionPointsExtension::fromArray([]);

    expect($ext->uris)->toBe([])
        ->and($ext->isCritical())->toBeFalse();
});

// --- AuthorityInfoAccessExtension ---

test('AuthorityInfoAccess: constructor sets OCSP and CA issuers URIs', function () {
    $ext = new AuthorityInfoAccessExtension(
        ocspUris: ['http://ocsp.example.com'],
        caIssuersUris: ['http://ca.example.com/ca.crt'],
    );

    expect($ext->ocspUris)->toBe(['http://ocsp.example.com'])
        ->and($ext->caIssuersUris)->toBe(['http://ca.example.com/ca.crt'])
        ->and($ext->isCritical())->toBeFalse();
});

test('AuthorityInfoAccess: oid and name', function () {
    expect(AuthorityInfoAccessExtension::oid())->toBe('1.3.6.1.5.5.7.1.1')
        ->and(AuthorityInfoAccessExtension::name())->toBe('authority-info-access');
});

test('AuthorityInfoAccess: toArray/fromArray round-trip', function () {
    $original = new AuthorityInfoAccessExtension(
        ocspUris: ['http://ocsp.example.com', 'http://ocsp2.example.com'],
        caIssuersUris: ['http://ca.example.com/ca.crt'],
        critical: false,
    );
    $array = $original->toArray();
    $restored = AuthorityInfoAccessExtension::fromArray($array);

    expect($restored->ocspUris)->toBe($original->ocspUris)
        ->and($restored->caIssuersUris)->toBe($original->caIssuersUris)
        ->and($restored->isCritical())->toBe($original->isCritical())
        ->and($array)->toBe([
            'name' => 'authority-info-access',
            'critical' => false,
            'ocsp_uris' => ['http://ocsp.example.com', 'http://ocsp2.example.com'],
            'ca_issuers_uris' => ['http://ca.example.com/ca.crt'],
        ]);
});

test('AuthorityInfoAccess: fromArray with empty data', function () {
    $ext = AuthorityInfoAccessExtension::fromArray([]);

    expect($ext->ocspUris)->toBe([])
        ->and($ext->caIssuersUris)->toBe([])
        ->and($ext->isCritical())->toBeFalse();
});

