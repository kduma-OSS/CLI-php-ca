// --- PrivateKeyUsagePeriodExtension ---

test('PrivateKeyUsagePeriod: constructor sets notBefore and notAfter', function () {
    $notBefore = new DateTimeImmutable('2024-01-01T00:00:00+00:00');
    $notAfter = new DateTimeImmutable('2025-01-01T00:00:00+00:00');
    $ext = new PrivateKeyUsagePeriodExtension(notBefore: $notBefore, notAfter: $notAfter);

    expect($ext->notBefore)->toEqual($notBefore)
        ->and($ext->notAfter)->toEqual($notAfter)
        ->and($ext->isCritical())->toBeFalse();
});

test('PrivateKeyUsagePeriod: notBefore and notAfter can be null', function () {
    $ext = new PrivateKeyUsagePeriodExtension;

    expect($ext->notBefore)->toBeNull()
        ->and($ext->notAfter)->toBeNull();
});

test('PrivateKeyUsagePeriod: oid and name', function () {
    expect(PrivateKeyUsagePeriodExtension::oid())->toBe('2.5.29.16')
        ->and(PrivateKeyUsagePeriodExtension::name())->toBe('private-key-usage-period');
});

test('PrivateKeyUsagePeriod: toArray filters null values', function () {
    $ext = new PrivateKeyUsagePeriodExtension(
        notBefore: new DateTimeImmutable('2024-01-01T00:00:00+00:00'),
    );
    $array = $ext->toArray();

    expect($array)->toHaveKey('not_before')
        ->and($array)->not->toHaveKey('not_after')
        ->and($array['name'])->toBe('private-key-usage-period');
});

test('PrivateKeyUsagePeriod: toArray/fromArray round-trip with both dates', function () {
    $original = new PrivateKeyUsagePeriodExtension(
        notBefore: new DateTimeImmutable('2024-01-01T00:00:00+00:00'),
        notAfter: new DateTimeImmutable('2025-12-31T23:59:59+00:00'),
        critical: true,
    );
    $array = $original->toArray();
    $restored = PrivateKeyUsagePeriodExtension::fromArray($array);

    expect($restored->notBefore->format('c'))->toBe($original->notBefore->format('c'))
        ->and($restored->notAfter->format('c'))->toBe($original->notAfter->format('c'))
        ->and($restored->isCritical())->toBe($original->isCritical());
});

test('PrivateKeyUsagePeriod: toArray/fromArray round-trip with no dates', function () {
    $original = new PrivateKeyUsagePeriodExtension;
    $array = $original->toArray();
    $restored = PrivateKeyUsagePeriodExtension::fromArray($array);

    expect($restored->notBefore)->toBeNull()
        ->and($restored->notAfter)->toBeNull();
});

test('PrivateKeyUsagePeriod: fromArray with empty data', function () {
    $ext = PrivateKeyUsagePeriodExtension::fromArray([]);

    expect($ext->notBefore)->toBeNull()
        ->and($ext->notAfter)->toBeNull()
        ->and($ext->isCritical())->toBeFalse();
});
